    private void btninsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertarActionPerformed
        if(cmbPrioridad.getSelectedIndex()==0){
            lblmensaje.setText("Favor de elegir una prioridad");
            return;
        }
        if(!OBJPrioridad.insertar(txtinsertar.getText().charAt(0), cmbPrioridad.getSelectedIndex())){
            lblmensaje.setText("Cola Llena");
            btninsertarIni.setEnabled(true);
            return;
        }
        mostrar();
    }//GEN-LAST:event_btninsertarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        if(!OBJPrioridad.eliminar()){
            lblmensaje.setText("Cola Vacia");
            btninsertarIni.setEnabled(false);
            return;
        }
        mostrar();
    }//GEN-LAST:event_btneliminarActionPerformed

    private void btninsertarIniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertarIniActionPerformed
       if(cmbPrioridad.getSelectedIndex()== 0){
           lblmensaje.setText("Favor de elegir prioridad");
           return;
       }
       if(!OBJPrioridad.insertarIni(txtinsertar.getText().charAt(0), cmbPrioridad.getSelectedIndex())){
           lblmensaje.setText("Cola llena por inicio");
           return;
       }
       mostrar();
    }//GEN-LAST:event_btninsertarIniActionPerformed

    private void btneliminarFinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarFinActionPerformed
       if(!OBJPrioridad.eliminarFin()){
           lblmensaje.setText("Cola vacia");
           btninsertarIni.setEnabled(false);
           return;
       }
       mostrar();
    }//GEN-LAST:event_btneliminarFinActionPerformed
      
    private void mostrar(){
        String cad ="";
        if(OBJPrioridad.getIni() == - 1){
            txtmostrar.setText("");
            return;
        }
        for(int i = OBJPrioridad.getIni(); i<= OBJPrioridad.getFin(); i++){
            //le da el formato [ini] = [valor, fin] ejemplo: [0] = [a,1]
            cad += String.format(" [%d] = [%s] ", i, OBJPrioridad.getDato(i));  
        }
        txtmostrar.setText(cad);
    }
        
